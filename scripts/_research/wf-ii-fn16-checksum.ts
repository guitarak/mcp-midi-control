/** wf-ii-fn16-checksum.ts (READ-ONLY) verify xor-7f envelope checksum on both fn16 frames */
const f0 = [0xf0,0x00,0x01,0x74,0x07,0x16,0x10,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x12,0x1c,0x04,0x00,0x00,0x00,0x7c,0x03,0x00,0x00,0x00,0x00,0x00,0x71,0xf7];
const f10= [0xf0,0x00,0x01,0x74,0x07,0x16,0x41,0x10,0x00,0x00,0x00,0x2c,0x0b,0x1f,0x39,0x03,0x0a,0x2e,0x0f,0x61,0x03,0x00,0x48,0x50,0x4b,0x04,0x00,0x00,0x00,0x00,0x00,0x59,0xf7];
function chk(f:number[]){
  // checksum byte is f[len-2]; cksum = xor of F0..last payload byte (i.e. f[0..len-3]) & 0x7f
  const cksumByte = f[f.length-2];
  let x=0; for(let i=0;i<=f.length-3;i++) x^=f[i];
  x&=0x7f;
  return {cksumByte:cksumByte.toString(16), computed:x.toString(16), ok:x===cksumByte};
}
console.log('P0 frame checksum:', JSON.stringify(chk(f0)));
console.log('P10 frame checksum:', JSON.stringify(chk(f10)));
